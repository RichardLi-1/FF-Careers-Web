const functions = require("firebase-functions");
const axios = require("axios");

exports.callChatGPT = functions.https.onRequest(async (req, res) => {
  if (req.method !== "POST") {
    return res.status(405).send("Method Not Allowed");
  }

  const { prompt } = req.body;

  if (!prompt) {
    return res.status(400).send("Missing 'prompt' in request body.");
  }

  try {
    const response = await axios.post(
      "https://api.openai.com/v1/chat/completions",
      {
        model: "gpt-3.5-turbo",
        messages: [{ role: "user", content: prompt }],
      },
      {
        headers: {
          "Content-Type": "application/json",
          Authorization: `Bearer ${functions.config().openai.key}`,
        },
      }
    );

    res.status(200).json(response.data);
  } catch (error) {
    console.error("‚ùå ChatGPT error:", error.response?.data || error.message);
    res.status(500).send("Failed to fetch ChatGPT response.");
  }
});